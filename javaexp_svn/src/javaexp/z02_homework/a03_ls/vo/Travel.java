package javaexp.z02_homework.a03_ls.vo;

public class Travel {
	String destination;
	int dayGo;
	int day;
	public Travel(String destination, int dayGo, int day) {
		this.destination = destination;
		this.dayGo = dayGo;
		this.day = day;
	}
	public void travelInfo() {
		System.out.println("# 여행 정보 #");
		System.out.println("목적지:"+destination);
		System.out.println("출발일:"+dayGo);
		System.out.println("도착일:"+day);
	}
	public int live() {
		return day-dayGo;
	}
}
