package javaexp.z02_homework.a06_psj.vo;

public class Car {
	int carNum;
	String model;
	int year;
	public Car(int carNum, String model) {
		this.carNum = carNum;
		this.model = model;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public void carInfo() {
		System.out.println("\n차량 번호: "+carNum);
		System.out.println("차량 모델: "+model);
		System.out.println("연식: "+year);
	}
}
