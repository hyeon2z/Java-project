package javaexp.z02_homework.a11_mhi.vo;

public class Pen {
	private String color;
	private String brand;
	private int price;

	public Pen(String color, String brand, int price) {
		this.color = color;
		this.brand = brand;
		this.price = price;
	}

	// 직접 입력
	public void showInf() {
		System.out.println("# pen객체의 정보 #");
		System.out.println("색상:" + color);
		System.out.println("브랜드:" + brand);
		System.out.println("가격:" + price);
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

}
