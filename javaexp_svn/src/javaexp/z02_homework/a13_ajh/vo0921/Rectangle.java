package javaexp.z02_homework.a13_ajh.vo0921;

public class Rectangle {
	private double width;
    private double height;
	public Rectangle(double width, double height) {
		this.width = width;
		this.height = height;
	}
	public void Rectangle1(double width, double height) {
        this.width = width;
        this.height = height;
    }
    public double calculateArea() {
        return width * height;
    }
    public double calculatePerimeter() {
        return 2 * (width + height);
    }
    public void printInfo() {
        System.out.println("가로 길이: " + width);
        System.out.println("세로 길이: " + height);
        System.out.println("넓이: " + calculateArea());
        System.out.println("둘레: " + calculatePerimeter());
    }
	public double getWidth() {
		return width;
	}
	public void setWidth(double width) {
		this.width = width;
	}
	public double getHeight() {
		return height;
	}
	public void setHeight(double height) {
		this.height = height;
	}
	

}
