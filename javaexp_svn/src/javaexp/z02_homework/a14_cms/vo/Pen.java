package javaexp.z02_homework.a14_cms.vo;

public class Pen {
	private String color;
	private String brand;
	private int price;
	
	public Pen(String color, String brand) {
		this.color = color;
		this.brand = brand;
	
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public void showPenInfo() {
		System.out.println("펜 브랜드는 : " + brand);
		System.out.println("펜 색은 : " + color);
		System.out.println("펜 가격은 : " + price);
	}

}
