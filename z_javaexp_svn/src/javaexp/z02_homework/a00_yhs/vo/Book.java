package javaexp.z02_homework.a00_yhs.vo;
// javaexp.z02_homework.a00_yhs.vo.Book
public class Book {
    private String title;
    private String author;
    private double price;
    private int publishYear;

    // 생성자
    public Book(String title, String author, double price, int publishYear) {
        this.title = title;
        this.author = author;
        this.price = price;
        this.publishYear = publishYear;
    }

    // 도서 정보 출력
    public void displayInfo() {
        System.out.println("Title: " + title + ", Author: " + author + ", Price: " + price + ", Published Year: " + publishYear);
    }

    // 가격 변경
    public void setPrice(double price) {
        this.price = price;
    }
}
