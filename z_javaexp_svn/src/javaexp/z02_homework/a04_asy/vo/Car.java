package javaexp.z02_homework.a04_asy.vo;

public class Car {
//		- 필드: 차량 번호, 모델, 연식
//    	- 생성자: 차량 번호, 모델 초기화
//    	- 메서드: 연식 변경, 정보 출력
	private String number; 
	private String model; 
	private int year;

	public Car(String number, String model, int year) {
		this.number=number;
		this.model=model;
		this.year = year;
	}
	public void carInfo() {
		System.out.println("# 차량 정보 # ");
		System.out.println("차량번호:"+number);
		System.out.println("차량모델:"+model);
		System.out.println("출시연도:"+year+"년");
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	
	
}	