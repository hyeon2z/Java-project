package javaexp.z02_homework.a19_cjw.vo;

public class Character {

	private String name;
	private int level;
	private int hp;

	public Character(String name, int level, int hp) {
		this.name = name;
		this.level = 1;
		this.hp = 1;
	}
	
	public int levelup() {
		level++;
		System.out.print("현재 레벨: ");
		return level;
	}
	
	public int recoverhp() {
		hp+=10;
		System.out.print("현재 체력: ");
		return hp;
	}
	
	public void charInfo() {
		System.out.println("이름: "+name);
		System.out.println("레벨: "+level);
		System.out.println("체력: "+hp);
	}
	
}
