package javaexp.z02_homework.a21_hcj.vo;
//6. **커피 클래스**
	//	    - 필드: 종류, 가격, 원산지
	//	    - 생성자: 종류 초기화
	//	    - 메서드: 가격 변경, 원산지 입력, 정보 출력
public class Coffee {
	private String type;
	private int price;
	private String form;
	
	public Coffee(String type) {
		this.type = type;
	}
	public void ShowInfo() {
		System.out.println("커피 정보");
		System.out.println("종류:"+type);
		System.out.println("가격:"+price);
		System.out.println("원산지:"+form);
	}
	public void setform(String form) {
		System.out.println("종류" +type+"의 원산지 등록완료");
		this.form = form;
	}
	public void setPrice(int price) {
		System.out.println(type+"의 가격 "+price+"원으로 변경완료");
		this.price = price;
	}
	
	
	
	
}
